package org.example;

public class Calculator {
    public void add(int a, int b) {
        System.out.println("total : "  + (a+b));
    }

    public void add(double a, double b) {
        System.out.println("total : "  + (a+b));
    }

    public void add(int a, int b, int c) {
        System.out.println("total : "  + (a+b+c));
    }
}
